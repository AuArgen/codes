import sys


def solve():
    # Маалыматтарды окуу
    input_data = sys.stdin.read().split()
    if not input_data:
        return

    N = int(input_data[0])

    nums = []

    # 1. Узундугу 2 болгон бардык кызыктуу сандар
    nums.append("11")
    pairs = [
        ("1", "2"), ("1", "4"), ("1", "6"),
        ("2", "3"), ("2", "5"), ("2", "9"),
        ("3", "4"), ("3", "8"),
        ("4", "7"), ("4", "9"),
        ("5", "6"), ("5", "8"),
        ("6", "7"),
        ("8", "9")
    ]
    for a, b in pairs:
        nums.append(a + b)
        nums.append(b + a)

    # 2. Узундугу 3төн баштап 50гө чейинки кызыктуу сандар
    # 2018-сан болжол менен 30-40 узундукта болот, андыктан 50 кенен жетет
    for L in range(3, 50):
        # Жалаң 1дерден турган сан
        nums.append("1" * L)

        # 1дердин арасына бир гана 2, 4 же 6 коюлган сандар
        for d in ['2', '4', '6']:
            for pos in range(L):
                s = "1" * pos + d + "1" * (L - 1 - pos)
                nums.append(s)

    # Сандарды узундугу боюнча, андан кийин алфавиттик (мааниси) боюнча өсүү тартибинде сорттоо
    nums.sort(key=lambda x: (len(x), x))

    # N-инчи санды чыгаруу (индекс 0дөн башталгандыктан N-1 кылабыз)
    print(nums[N - 1])


if __name__ == '__main__':
    solve()