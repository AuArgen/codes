import sys


def solve():
    # Маалыматты тез окуу
    input_data = sys.stdin.read().split()
    if not input_data:
        return

    # N - көп бурчтуктун периметри (4төн 10^6 га чейин)
    n = int(input_data[0])

    # Эгерде периметр так сан болсо, андай көп бурчтук болуусу мүмкүн эмес
    if n % 2 != 0:
        print("-1")
    else:
        # Жарым периметрди табабыз (узуну + туурасы)
        p = n // 2

        # Аянт эң чоң болушу үчүн тараптарды бири-бирине жакын кылабыз
        h = p // 2
        w = p - h

        # Аянтты эсептеп, 1000ге бөлгөндөгү калдыкты табабыз
        s = (h * w) % 1000

        print(s)


if __name__ == '__main__':
    solve()